<?php


namespace Kyanag\Form;

/**
 * 组件常用属性
 * Class Attributes
 * @package Kyanag\Form
 */
class Attributes
{

    /**
     * @var array|string
     */
    public $class = null;

    /**
     * @var string
     */
    public $style = null;

    /**
     * @var string
     */
    public $placeholder = null;

    /**
     * @var mixed
     */
    public $default = null;

    /**
     * @var bool
     */
    public $hide = false;

    /**
     * @var bool
     */
    public $multiple = false;

    /**
     * @var bool
     */
    public $disable = false;

    /**
     * @var bool
     */
    public $readonly = false;

    /**
     * @var bool
     */
    public $repeated = false;

    /**
     * @var bool
     */
    public $sortable = false;

    /**
     * @var bool
     */
    public $searchable = false;

    /**
     * @var bool
     */
    public $resetable = false;
}